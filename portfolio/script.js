// JavaScriptを使ってタイトルを装飾
document.addEventListener("DOMContentLoaded", function() {
    var titleElement = document.getElementById("title");
    titleElement.style.fontFamily = "Arial, sans-serif"; // フォントファミリーを指定
    titleElement.style.fontSize = "36px"; // フォントサイズを指定
    titleElement.style.color = "#FF5733"; // テキストの色を指定
    titleElement.style.textShadow = "2px 2px 4px #000"; // テキストに影を追加
});
